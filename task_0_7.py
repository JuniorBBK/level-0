def celsius_to_fahreinheits(celsius):
    fahrenheits = (9/5 * celsius) + 32
    return(f'{celsius} degrees Celsius is equal to {fahrenheits} degrees Fahrenheits')

def fahreinheits_to_celsius(fahreinheits):
    celsius = (fahreinheits - 32) * 5/9
    return(f'{fahreinheits} degrees fahreinheits is equal to {celsius} degrees celsius')


